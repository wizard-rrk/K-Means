package com.wizard.kmeans;

public class Cluster {

	
	double x;
	double y;
	
	public Cluster(double x2, double y2) {
		super();
		this.x = x2;
		this.y = y2;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
}
